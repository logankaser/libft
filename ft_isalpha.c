int	ft_isalpha(char *str)
{
	char	*chr;

	chr = str;
	while (*chr)
	{
		if (*chr < 'A' || (*chr > 'Z' && *chr < 'a') || *chr > 'z')
			return (0);
		++chr;
	}
	return (1);
}
